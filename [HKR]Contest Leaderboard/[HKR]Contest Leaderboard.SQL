SELECT A.HACKER_ID, A.NAME, SUM(SCORE) SUM_SOCRE
 FROM (SELECT HACKER_ID, CHALLENGE_ID, max(SCORE) SCORE
      FROM SUBMISSIONS
      GROUP BY HACKER_ID, CHALLENGE_ID) B
JOIN HACKERS A
  ON A.HACKER_ID = B.HACKER_ID
GROUP BY A.HACKER_ID, A.NAME
HAVING SUM(SCORE)>0
ORDER BY SUM_SOCRE DESC, A.HACKER_ID
      